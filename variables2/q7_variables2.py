# Q.7 What happens when you run the following code? Why?

NAME = 'Victor'
print('Good Morning, ' + NAME)
print('Good Afternoon, ' + NAME)
print('Good Evening, ' + NAME)

NAME = 'Nina'
print('Good Morning, ' + NAME)
print('Good Afternoon, ' + NAME)
print('Good Evening, ' + NAME)

# It greets 'Victor' three times by using the variable.
# It greets 'Nina' three times by using a variable.

# Python still runs even though the constant variable NAME is used twice. That's because Python does not have
# real constants, and it is the responsibility of the programmer to highlight the use of a constant in a program.


